let sigla = "Rj".toUpperCase();
switch (sigla) {
    case "RJ":
        console.log("Carioca");
        break;
        case "SP":
            console.log("Paulista");
            break;
            case "MG":
            console.log("Mineiro");
            break;
            default:
                console.log("Outros Estados");
}